        static void Sort(int[] arr, int start, int finish)
        {
            int i = start;
            int j = finish;
            int elem = arr[(start + finish) / 2];
            int temp;
            while (i <= j)
            {
                while (arr[i] > elem)
                {
                    i++;
                }
                while (arr[j] < elem)
                {
                    j--;
                }
                if (i <= j)
                {
                    temp = arr[i];
                    arr[i] = arr[j];
                    arr[j] = temp;
                    i++;
                    j--;
                }
            }
            if (i < finish)
            {
                Sort(arr, i, finish);
            }
            if (j > start)
            {
                Sort(arr, start, j);
            }
        }
